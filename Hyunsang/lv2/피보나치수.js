function solution(n) {
    let answer = 0;  // 피보나치 수열 결과를 저장할 변수
    let f0 = 0;      // 첫 번째 피보나치 수 (0번째)
    let f1 = 1;      // 두 번째 피보나치 수 (1번째)
    
    // 피보나치 수열을 반복문으로 계산, n번째 수를 구할 때까지 반복
    for(let i = 2; i <= n; i++){
        // 이전 두 수의 합을 1234567로 나눈 나머지를 저장
        answer = (f0 + f1) % 1234567;
        
        // 다음 계산을 위해 f0과 f1을 갱신
        f0 = f1;
        f1 = answer;
    }
    
    return answer;  // n번째 피보나치 수 반환
}

/**
 * 시간복잡도 : O(n)
 */